'use client'

import { cn } from '@cgk/ui'
import { useCallback } from 'react'

import { BRAND_CATEGORIES } from '@/lib/brand-preferences/constants'
import type { BrandCategory } from '@/lib/types'

interface CategorySelectorProps {
  selected: BrandCategory[]
  onChange: (categories: BrandCategory[]) => void
  disabled?: boolean
}

/**
 * CategorySelector - Multi-select grid of brand categories
 *
 * Features:
 * - Visual category chips with icons
 * - Toggle selection on click
 * - Subtle animations on selection
 */
export function CategorySelector({
  selected,
  onChange,
  disabled = false,
}: CategorySelectorProps): React.JSX.Element {
  const handleToggle = useCallback(
    (category: BrandCategory) => {
      if (disabled) return

      if (selected.includes(category)) {
        onChange(selected.filter((c) => c !== category))
      } else {
        onChange([...selected, category])
      }
    },
    [selected, onChange, disabled]
  )

  return (
    <div className="grid grid-cols-2 gap-2 sm:grid-cols-3 lg:grid-cols-4">
      {BRAND_CATEGORIES.map((category) => {
        const isSelected = selected.includes(category.id)

        return (
          <button
            key={category.id}
            type="button"
            onClick={() => handleToggle(category.id)}
            disabled={disabled}
            className={cn(
              'group relative flex items-center gap-2.5 rounded-lg border px-3 py-2.5 text-left text-sm transition-all',
              'focus:outline-none focus-visible:ring-2 focus-visible:ring-primary focus-visible:ring-offset-2',
              isSelected
                ? 'border-primary/50 bg-primary/5 text-foreground shadow-sm'
                : 'border-border bg-card text-muted-foreground hover:border-primary/30 hover:bg-accent/50',
              disabled && 'cursor-not-allowed opacity-60'
            )}
          >
            {/* Selection indicator */}
            <span
              className={cn(
                'flex h-5 w-5 shrink-0 items-center justify-center rounded-full border-2 transition-all',
                isSelected
                  ? 'border-primary bg-primary text-primary-foreground'
                  : 'border-muted-foreground/30 bg-background'
              )}
            >
              {isSelected && (
                <svg
                  xmlns="http://www.w3.org/2000/svg"
                  viewBox="0 0 24 24"
                  fill="none"
                  stroke="currentColor"
                  strokeWidth="3"
                  strokeLinecap="round"
                  strokeLinejoin="round"
                  className="h-3 w-3"
                >
                  <path d="M20 6 9 17l-5-5" />
                </svg>
              )}
            </span>

            {/* Category icon placeholder */}
            <CategoryIcon name={category.icon} className="h-4 w-4 shrink-0" />

            {/* Label */}
            <span className="truncate font-medium">{category.label}</span>
          </button>
        )
      })}
    </div>
  )
}

