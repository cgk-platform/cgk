interface PortalIconProps {
  /** Icon key from the portal icon set */
  iconKey: string
  /** Size in pixels */
  size?: number
  /** Stroke width */
  strokeWidth?: number
  /** Additional CSS classes */
  className?: string
  /** Custom color (defaults to currentColor) */
  color?: string
  /** Custom inline styles */
  style?: CSSProperties
  /** Accessible label */
  'aria-label'?: string
  /** Whether icon is decorative (default: true) */
  decorative?: boolean
}

/**
 * Portal Icon Component
 *
 * Renders SVG icons with consistent styling across the portal.
 */
export function PortalIcon({
  iconKey,
  size = 24,
  strokeWidth = 1.5,
  className,
  color,
  style,
  'aria-label': ariaLabel,
  decorative = true,
}: PortalIconProps): React.ReactElement {
  const icon = PORTAL_ICONS[iconKey] || FALLBACK_ICON

  return (
    <svg
      className={cn('shrink-0', className)}
      width={size}
      height={size}
      viewBox="0 0 24 24"
      fill="none"
      stroke={color || 'currentColor'}
      strokeWidth={strokeWidth}
      style={style}
      aria-hidden={decorative}
      aria-label={!decorative ? ariaLabel : undefined}
      role={!decorative ? 'img' : undefined}
    >
      {icon}
    </svg>
  )
}

/**
 * Get list of available icon keys
 */
export function getAvailableIconKeys(): string[] {
  return Object.keys(PORTAL_ICONS)
}

/**
 * Check if an icon key exists
 */
export function hasIcon(iconKey: string): boolean {
  return iconKey in PORTAL_ICONS
}
